//exsample :: do_shortcode("[supsystic-tables-cell id=3 row=2 col=B]")
//do_shortcodeの擬似プログラム
function do_shortcode($content) {
    $results = [];
    preg_match("/^.*\s(id=[\w]*)\s(row=[\w]*)\s(col=[\w]*)/", $content, $matches);
    if(count($matches) === 4) {
        foreach(array_slice($matches, 1) as $value) {
            $result = explode("=", $value);
            $results[ $result[0] ] = $result[1];
        }
    }

    // sheetIdが999の時、常に空文字を返すようにする。テストコード
    if ((int) $results["id"] == 999) {
        return "";
    }
    return test_data_store($results["id"], (int)$results["row"], $results["col"]);
}

function test_data_store($id, $row, $col) {
    global $test_data;
    $col = getPosAlphabetNumber($col, 2);

    $val =(String)$test_data[$row - 1][$col - 1];
    if ($val == "") {
        return "No value";
    }
    return $val;
}

function getPosAlphabetNumber($alphabet, $length)
{
    if($length * 26 >= strlen($alphabet)) {
        $pos = array_search($alphabet, generate($length));
        if($pos !== false) {
            return $pos + 1;
        }
    }
    return null;
}

function generate($length) {
    $list = [];
    $maxLimit = $length * 26;
    $count = 1;
    for($a = 'A' ; ; $a++) {
        $list[] = $a;
        $count++;
        if($maxLimit < $count) break;
    }
    return $list;
}

